#include <bits/stdc++.h>
using namespace std;

const string data = "+-------------------------------------------------+\n"
                    "|                                                 |\n"
                    "|    H######                      ####            |\n"
                    "|          #                      #  #            |\n"
                    "|          #                      #  #            |\n"
                    "|          #     ####             #  #            |\n"
                    "|          #     #  #             #  #            |\n"
                    "|          ######@###             #  #            |\n"
                    "|                #       ####     #  #            |\n"
                    "|                #       #  #     #  #            |\n"
                    "|            ####@#######@###     #  #            |\n"
                    "|            #   #       #        #  #            |\n"
                    "| T          #####       #        #  #   ##       |\n"
                    "| #                      #      ###  ### ##       |\n"
                    "| ################       #      #      ####       |\n"
                    "|                #       #      #         #       |\n"
                    "|   ##############       #######@##########       |\n"
                    "|   #                         ###                 |\n"
                    "|   ###########################                   |\n"
                    "+-------------------------------------------------+";

int main() {
    int sum = 0;
    for (int i = 0; i < data.size(); i++) {
        switch (data[i]) {
            case 'H':
                sum++;
                break;
            case 'T':
                sum++;
                break;
            case '#':
                sum++;
                break;
            case '@':
                sum += 2;
                break;
        }
    }
    cout << sum;

    return 0;
}